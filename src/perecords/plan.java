  public int returnPlId(){
      int tab=tabPl.getSelectedIndex();
      String val="";
      switch(tab){
          case 0:
              val=this.lstPlnWait.getSelectedValue().toString();
              break;
          case 1:
              val=this.lstPlnAch.getSelectedValue().toString();
              break;
          case 2:
              val=this.lstPlnFail.getSelectedValue().toString();
              break;
          default:
              val=this.lstPlnAch.getSelectedValue().toString();
      }
      return records.returnID("SELECT * FROM plan WHERE pl_title='"+val+"'");
  }
