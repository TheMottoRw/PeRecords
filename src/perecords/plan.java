    private void btnDelPlnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelPlnActionPerformed
        try{
            int paneInd=tabPl.getSelectedIndex();
        String panels[]=new String[]{"waiting","achieved","fail"};
        switch(panels[paneInd]){
            case"waiting":
                records.selectedPlanTitle=lstPlnWait.getSelectedValue().toString();
                        break;
             case"achieved":
                     records.selectedPlanTitle=lstPlnAch.getSelectedValue().toString();
                        break;
            case"fail":
                records.selectedPlanTitle=lstPlnFail.getSelectedValue().toString();
                        break;
        }
        int plnid=records.returnID("SELECT * FROM plan WHERE pl_title='"+records.selectedPlanTitle+"'");
        records.deletePlanBudget(plnid,"plan");records.deletePlanSource(plnid,"plan");records.deletePlanProfit(plnid,"plan");
        records.deletePlan(plnid);
        records.waitingPlan(lstPlnWait);
      records.achievedPlan(lstPlnAch);
      records.failedPlan(lstPlnFail);
      }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Sorry an Error Occurred "+ex);
        }
    }//GEN-LAST:event_btnDelPlnActionPerformed

